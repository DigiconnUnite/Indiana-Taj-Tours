<script>
    // Hide preloader when page is fully loaded
    window.addEventListener('load', function() {
        var preloader = document.getElementById('preloader');
        if (preloader) {
            preloader.style.display = 'none';
        }
        document.body.style.overflow = '';
    });

    // Handle contact form submission with AJAX
    document.getElementById('contactForm')?.addEventListener('submit', function(e) {
        e.preventDefault();

        const form = this;
        const submitBtn = form.querySelector('button[type="submit"]');
        const successAlert = document.getElementById('contactSuccess');

        // Disable submit button and show loading
        submitBtn.disabled = true;
        submitBtn.innerHTML = '<i class="fas fa-spinner fa-spin me-2"></i>Sending...';

        // Prepare form data
        const formData = new FormData(form);

        // Send AJAX request
        fetch('main.php', {
            method: 'POST',
            body: formData
        })
        .then(response => response.json())
        .then(data => {
            if (data.success) {
                // Show success message
                successAlert.className = 'alert alert-success mt-3';
                successAlert.textContent = data.message;
                successAlert.classList.remove('d-none');

                // Reset form
                form.reset();

                // Hide success message after 10 seconds
                setTimeout(() => {
                    successAlert.classList.add('d-none');
                }, 10000);
            } else {
                // Show error message
                successAlert.className = 'alert alert-danger mt-3';
                successAlert.textContent = data.message;
                successAlert.classList.remove('d-none');
            }
        })
        .catch(error => {
            console.error('Error:', error);
            successAlert.className = 'alert alert-danger mt-3';
            successAlert.textContent = 'An error occurred. Please try again later.';
            successAlert.classList.remove('d-none');
        })
        .finally(() => {
            // Re-enable submit button
            submitBtn.disabled = false;
            submitBtn.innerHTML = 'Send Message';
        });
    });
</script>

